package Naveen_Package;

public class MathmaticalConcept {

	public static void main(String[] args) {
		
		
		System.out.println(10/2);
		System.out.println(11/2);
		System.out.println(11/2.0);
		System.out.println(11.0/2);
		System.out.println(11/2f);
		System.out.println(11/2);
		
//		System.out.println(11/0);
		System.out.println(0/11);
	

		double d = 11/2;
		System.out.println(d);
		
	double e = 11/2d;
	System.out.println(e);
		
	float f = 11/2;
	System.out.println(f);
	
	float g = 11/2f;
	System.out.println(g);
	
	
	System.out.println("Aman" + "\t");
	
	System.out.println("AZZZZ"+ "\b"+ "a");
	
	}

}
